    // Modify the Add buttion
    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String name = "'", ID = "", address = "", gender = "", position = "";
        int exe = 0;
        long phoneNumber = 0;
        boolean isOK = true;
        
        // Name
        name = txtName.getText();
        if (name.length() == 0 ) {
            JOptionPane.showMessageDialog(rootPane, "Name cannot left blank!");
            isOK = false;
        } else if (name.matches("[^a-zA-Z]+")) {
            JOptionPane.showMessageDialog(rootPane, "Invalid name! Name only have the character!");
            isOK = false;
        }
        
        // ID
        ID = txtID.getText();
        if (!ID.equals("MA1") && !ID.equals("MA2") && !ID.equals("S1") && !ID.equals("S2") && !ID.equals("S3") && !ID.equals("S4") && !ID.equals("S5") ) {
            JOptionPane.showMessageDialog(rootPane, """
                                                    The ID should be MA1 and MA2 for two managers
                                                    S1, S2, S3, S4, S5 for the staff""");
            isOK = false;
        } 

        
        // Phone
        try {
            phoneNumber = Long.parseLong(txtPhoneNumber.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Wrong format of phone numbers!");
            isOK = false;
        }
        if (String.valueOf(phoneNumber).length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Phone number cannot left blank!");
            isOK = false;
        } else if (String.valueOf(phoneNumber).length() != 9) {
            JOptionPane.showMessageDialog(rootPane, "Phone number may have 10 numbers!");
            isOK = false;
        }
        String phoneNumber1 = "0" + phoneNumber;
        
        // Gender
        gender = optGender.getSelectedItem().toString();
        
        // Address
        address = txtAddress.getText();
        if (address.length() == 0 ) {
            JOptionPane.showMessageDialog(rootPane, "Address cannot left blank!");
            isOK = false;
        }
        
        // Position
        position = optPosition.getSelectedItem().toString();
        if (position.equals("Manager") && (ID.matches(".*S.*"))) {
            JOptionPane.showMessageDialog(rootPane, """
                                                    Wrong ID for manager!
                                                    The ID should be start with M with (a number 1 or 2)""");
            isOK = false;
        } else if (position.equals("Staff") && (ID.matches(".*MA.*"))) {
            JOptionPane.showMessageDialog(rootPane, """
                                                    Wrong ID for Staff!
                                                    The ID should be start with S with (a number from 1 to 5)""");
            isOK = false;
        }
        

        
        // DoB
        Date dob = dateDob.getDate();

        // Experience
        try {
            exe = Integer.parseInt(txtExe.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, "Wrong format for experience!");
            isOK = false;
        }
        
        // Start work from
        Date workFrom = dateWorkFrom.getDate();

        if (isOK) {
            Information information = new Information(name, ID, phoneNumber1, gender, dob, address, workFrom, exe, position);
            main.addInformation(information);
            JOptionPane.showMessageDialog(rootPane, "Successfully added!");
        }
    }//GEN-LAST:event_btnAddActionPerformed

